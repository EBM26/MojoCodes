	$(".citraLearn").click(function(){

		if(!$(this).hasClass("active")) {
			$(".learnButton").removeClass("active");
			$(".moreDiv").slideUp("slow");
			$(".learnIcon").removeClass("transition");
			$(this).addClass("active");
			$(".CitraMoreDiv").slideDown("slow");
			$(".CitraLearnIcon").addClass("transition");
			}

		    else if($(this).hasClass("active")) {
				$(this).removeClass("active");
				$(".moreDiv").slideUp("slow");
				$(".learnIcon").removeClass("transition");
			  }

	});

